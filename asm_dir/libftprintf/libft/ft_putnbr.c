#include "libft.h"

void	ft_putnbr(int n)
{
	long int	nl;

	nl = n;
	if (nl < 0)
	{
		ft_putchar('-');
		nl *= -1;
	}
	if (nl / 10)
		ft_putnbr(nl / 10);
	ft_putchar(nl % 10 + '0');
}
