#include "libft.h"

int		ft_str_allnum(char *str)
{
	int	res;
	int	i;

	res = 1;
	i = -1;
	while (str[++i])
	{
		if ((str[i] == '-' && i == 0) || (str[i] >= '0' && str[i] <= '9'))
			continue ;
		else
			return (0);
	}
	return (res);
}
