#include "vm.h"

int		ft_atoi_vm(const char *str)
{
	int		i;
	int		res;

	res = 0;
	i = 0;
	while (str[i])
	{
		if ((str[i] < '0') || (str[i] > '9'))
			ft_usage_vm();
		res = res * 10 + str[i++] - '0';
	}
	return (res);
}
